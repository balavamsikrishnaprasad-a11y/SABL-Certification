/* ================= BULK GENERATOR ================= */

async function generateBulkCertificates() {

  if (bulkRolls.length !== bulkNames.length) {
    alert("Roll numbers and names count mismatch!");
    return;
  }

  form.style.display = "none";
  certificate.style.display = "block";
  download.style.display = "none";

  for (let i = 0; i < bulkRolls.length; i++) {

    fields.name.value = bulkNames[i];
    fields.roll.value = bulkRolls[i];

    await generateSingleCertificate();

    // small delay to avoid browser blocking
    await new Promise(resolve => setTimeout(resolve, 800));
  }

  alert("All certificates generated successfully!");
}